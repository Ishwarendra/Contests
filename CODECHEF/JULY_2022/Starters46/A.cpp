using i64 = long long;

void solve()
{
    int n;
    std::string s;
    std::cin >> n >> s;

    std::vector<int> pos;

    for (int i = 1; i < n; i++)
    {
        if (s[i] == '0' and s[i - 1] == '1')
            pos.push_back(i);
    }

    std::cout << std::size(pos) << "\n";
}

