using i64 = long long;

void solve()
{
    int n, m;
    std::cin >> n >> m;
    
    i64 ans = 0;
    for (int g = 1; g <= std::min(n, m); g++)
        ans += (n / g + 1) / g;
    std::cout << ans - 1 << "\n";
}

int main()
{
    std::ios::sync_with_stdio(false);
    std::cin.tie(nullptr);

    int t;
    std::cin >> t;
    
    while (t--)
        solve();
    
    return 0;
}