using i64 = long long;

void solve()
{
    int n;
    std::string s;
    std::cin >> n >> s;

    int ans = 0, cnt = 0;  
    for (auto ch : s)
    {
        if (ch == '*' and cnt == 1)
            break;
        if (ch == '*')
            cnt++;
        else
            cnt = 0, ans += ch == '@';
    }

    std::cout << ans << "\n";
}

int main()
{
    std::ios::sync_with_stdio(false);
    std::cin.tie(nullptr);

    int t;
    std::cin >> t;
    
    while (t--)
        solve();
    
    return 0;
}