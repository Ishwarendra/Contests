using i64 = long long;

void solve()
{
    int n;
    std::cin >> n;

    std::vector<int> a(n);
    for (int i = 0; i < n; i++)  
        std::cin >> a[i];

    auto sum = std::accumulate(std::begin(a), std::end(a), 0LL);
    int cnt = std::count(std::begin(a), std::end(a), 1);
    sum -= cnt * 2;

    std::cout << (n > 1 and sum >= (n - cnt) ? "YES\n" : "NO\n");
}

int main()
{
    std::ios::sync_with_stdio(false);
    std::cin.tie(nullptr);

    int t;
    std::cin >> t;
    
    while (t--)
        solve();
    
    return 0;
}