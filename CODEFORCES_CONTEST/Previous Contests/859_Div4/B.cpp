using i64 = long long;

void solve()
{
    int n;
    std::cin >> n;

    std::vector<int> a(n);
    for (int i = 0; i < n; i++)  
        std::cin >> a[i];

    std::array<i64, 2> sum{};
    for (int i = 0; i < n; i++)
        sum[a[i] % 2] += a[i];

    std::cout << (sum[0] > sum[1] ? "YES\n" : "NO\n");
}

