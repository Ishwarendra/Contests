using i64 = long long;

void solve()
{
    int n;
    std::cin >> n;

    std::vector<int> f(n + 1);
    int ans = 0;
    for (int i = 0; i < n; i++)
    {
        int x;
        std::cin >> x;
        f[x]++;
        ans += (f[x] == 2);
    }

    std::cout << ans << "\n";
}

