using i64 = long long;

void solve()
{
    int n;
    std::string s;
    std::cin >> n >> s;

    std::string ans;

    for (int i = 0; i < n; i++)
    {
        char ch = s[i];
        ans += s[i];
        i++;
        while (i < n and s[i] != ch)
            i++;
    }

    std::cout << ans << "\n";
}

int main()
{
    std::ios::sync_with_stdio(false);
    std::cin.tie(nullptr);

    int t;
    std::cin >> t;

    while (t--)
        solve();

    return 0;
}