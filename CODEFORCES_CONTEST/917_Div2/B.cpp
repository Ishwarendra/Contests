using i64 = long long;

void solve()
{
    int n;
    std::string s;
    std::cin >> n >> s;

    std::set<char> st;
    i64 ans = 0;

    for (int i = 0; i < n; i++)
    {
        st.emplace(s[i]);
        ans += std::size(st);
    }

    std::cout << ans << "\n";
}

int main()
{
    std::ios::sync_with_stdio(false);
    std::cin.tie(nullptr);

    int t;
    std::cin >> t;
    
    while (t--)
        solve();
    
    return 0;
}